// Looping Statement  or Control Flow statement


//  Write a java program to print number from 1 to 5 using while , do while, for loop

// do while loop // 

class DoWhile {

public static void main(String args [ ] ) {

int a; 
a = 20;// initialization

do {
System.out.println(a);
a++;
}
while( a <= 10);

}
}
