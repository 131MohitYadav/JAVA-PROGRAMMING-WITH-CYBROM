// Looping Statement  or Control Flow statement


//  Write a java program to print 10 to 20 even number by using do while loop

// do while loop // 

class DoWhile1 {

public static void main(String args [ ] ) {

int i ;
i = 10;
do{
if ( i % 2 == 0 ){
System.out.println(i);
} 
i++;
}
while( i <= 20);

}
}
